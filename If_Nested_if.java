package java_prac;

public class If_Nested_if {

	public static void main(String[] args) 
	{
	
		int a,b;
		a =10;
		b =10;
		if(a>b)
		{
			System.out.println("a is big");
		}
		else if(b>a)
		{
			System.out.println("b is big");
			
		}
		else
		{
			System.out.println("both are same");
		}

	}

}
